    public boolean validaEmpty(){
        if (jTADescricao.getText().equals("") || jTADescricao.getText().startsWith(" ")){
            JOptionPane.showMessageDialog(rootPane, "Nome não informado");
            jTADescricao.requestFocus();
            return false;
        }else if (jFTFPreco.getText().equals("") || jFTFPreco.getText().startsWith(" ")){
            JOptionPane.showMessageDialog(rootPane, "Email não informado");
            jFTFPreco.requestFocus();
            return false;
        }else if (jFTFEstoque.getText().equals("") || jFTFEstoque.getText().startsWith(" ")){
            JOptionPane.showMessageDialog(rootPane, "Fone não informado");
            jFTFEstoque.requestFocus();
            return false;
        }else{
            return true;
        }
    }
    
    public void limpar() {
        jTADescricao.setText("");
        jFTFPreco.setText("");
        jFTFEstoque.setText("");
        jFTFPreco.requestFocus();
        
    }
    
