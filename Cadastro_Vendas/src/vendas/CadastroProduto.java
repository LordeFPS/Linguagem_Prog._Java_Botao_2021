    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        float preco = Float.parseFloat(jFTFPreco.getText());
        float estoque = Float.parseFloat(jFTFEstoque.getText());
        String descriacao = jTADescricao.getText();
        
        Produto produto = new Produto(descriacao, preco, estoque);
        
        boolean validade = validaEmpty();
        
        if (validade == true){
            JOptionPane.showMessageDialog(rootPane, "Produto inserido com sucesso");
            limpar();
            if (produto.salvar()){
                limpar();
            }else {
                JOptionPane.showMessageDialog(rootPane, "Erro ao cadastro produto");
            }
        }
    }//GEN-LAST:event_jButton2ActionPerformed

