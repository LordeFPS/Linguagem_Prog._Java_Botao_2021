    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        try {
            
            DefaultTableModel dtm = (DefaultTableModel) jTableContato.getModel();
            dtm.setNumRows(0);
            Contato contato = new Contato();
            ResultSet rs = contato.getAll(jTFFilter.getText());

            while (rs.next()){
            
                dtm.addRow(new Object[]{rs.getString("codcontato"),
                                        rs.getString("nome"),
                                        rs.getString("fone"),
                                        rs.getString("email")
                                        });
            }
            } catch (SQLException ex) {
                Logger.getLogger(ConsultaContatos.class.getName()).log(Level.SEVERE, null, ex);
            }
    }//GEN-LAST:event_jButton1ActionPerformed

